package Chapter_2_2;

public class GroceryListItem {
    public String name;
    public String category;
    public double cost;
    public int quantity;

    public GroceryListItem(String name, String category, double cost, int quantity) {
        this.name = name;
        this.category = category;
        this.cost = cost;
        this.quantity = quantity;
    }
}
